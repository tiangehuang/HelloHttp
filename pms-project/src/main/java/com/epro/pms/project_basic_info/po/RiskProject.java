package com.epro.pms.project_basic_info.po;
/**
 * 
 * @author qinshiguang
 * VI.2.10	RISK_PROJECT 重点/风险项目po
 */
public class RiskProject 
{
	private Integer projectId;		//项目ID
	private String projectName;		//项目名称
	private String projVersion;		//项目版本
	private String riskType;		//风险项目类型
	private String orderlies;		//看护人
	
	private String riskIssues;		//风险问题内容
	private String riskStatus;		//风险状态
	private String isRiskAscend;	//问题/风险是否上升
	private String poStatusRisk;	//PO看护状态及风险
	
	//项目表的字段
	private String product;			//所属产品
	private String area;			//地域
	private String productLine;		//产品线 
	
	
	public Integer getProjectId()
	{
		return projectId;
	}
	public void setProjectId(Integer projectId)
	{
		this.projectId = projectId;
	}
	public String getProjectName()
	{
		return projectName;
	}
	public void setProjectName(String projectName)
	{
		this.projectName = projectName;
	}
	public String getProjVersion()
	{
		return projVersion;
	}
	public void setProjVersion(String projVersion)
	{
		this.projVersion = projVersion;
	}
	public String getRiskType()
	{
		return riskType;
	}
	public void setRiskType(String riskType)
	{
		this.riskType = riskType;
	}
	public String getOrderlies()
	{
		return orderlies;
	}
	public void setOrderlies(String orderlies)
	{
		this.orderlies = orderlies;
	}
	public String getRiskIssues()
	{
		return riskIssues;
	}
	public void setRiskIssues(String riskIssues)
	{
		this.riskIssues = riskIssues;
	}
	public String getRiskStatus()
	{
		return riskStatus;
	}
	public void setRiskStatus(String riskStatus)
	{
		this.riskStatus = riskStatus;
	}
	public String getIsRiskAscend()
	{
		return isRiskAscend;
	}
	public void setIsRiskAscend(String isRiskAscend)
	{
		this.isRiskAscend = isRiskAscend;
	}
	public String getPoStatusRisk()
	{
		return poStatusRisk;
	}
	public void setPoStatusRisk(String poStatusRisk)
	{
		this.poStatusRisk = poStatusRisk;
	}
	public String getProduct()
	{
		return product;
	}
	public void setProduct(String product)
	{
		this.product = product;
	}
	public String getArea()
	{
		return area;
	}
	public void setArea(String area)
	{
		this.area = area;
	}
	public String getProductLine()
	{
		return productLine;
	}
	public void setProductLine(String productLine)
	{
		this.productLine = productLine;
	}
	@Override
	public String toString()
	{
		return "RiskProject [projectId=" + projectId + ", projectName=" + projectName + ", projVersion=" + projVersion
				+ ", riskType=" + riskType + ", orderlies=" + orderlies + ", riskIssues=" + riskIssues + ", riskStatus="
				+ riskStatus + ", isRiskAscend=" + isRiskAscend + ", poStatusRisk=" + poStatusRisk + ", product="
				+ product + ", area=" + area + ", productLine=" + productLine + "]";
	}
}
